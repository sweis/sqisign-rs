/// `2^TORSION_EVEN_POWER` as an Ibz (lazy-initialized; matches C's
/// `TORSION_PLUS_2POWER` precomp).
pub fn torsion_plus_2power() -> &'static Ibz {
    static T: OnceLock<Ibz> = OnceLock::new();
    T.get_or_init(|| {
        let mut z = Ibz::default();
        ibz_copy_digits(&mut z, &TORSION_PLUS_2POWER);
        z
    })
}

