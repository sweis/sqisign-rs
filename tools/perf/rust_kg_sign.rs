// Controlled keygen/sign benchmark on KAT-0 seed, mirrors /tmp/c_kgsign_bench.c.
// Modes: "kg" -> 20x { reseed; keypair }, "kgsign" -> 20x { reseed; keypair; sign }.

use sqisign_rs::common::ctrdrbg;
use sqisign_rs::nistapi::{crypto_sign, crypto_sign_keypair};

const SEED: [u8; 48] = [
    0x06, 0x15, 0x50, 0x23, 0x4D, 0x15, 0x8C, 0x5E, 0xC9, 0x55, 0x95, 0xFE, 0x04, 0xEF, 0x7A,
    0x25, 0x76, 0x7F, 0x2E, 0x24, 0xCC, 0x2B, 0xC4, 0x79, 0xD0, 0x9D, 0x86, 0xDC, 0x9A, 0xBC,
    0xFD, 0xE7, 0x05, 0x6A, 0x8C, 0x26, 0x6F, 0x9E, 0xF9, 0x7E, 0xD0, 0x85, 0x41, 0xDB, 0xD2,
    0xE1, 0xFF, 0xA1,
];
const MSG: [u8; 33] = [
    0xD8, 0x1C, 0x4D, 0x8D, 0x73, 0x4F, 0xCB, 0xFB, 0xEA, 0xDE, 0x3D, 0x3F, 0x8A, 0x03, 0x9F,
    0xAA, 0x2A, 0x2C, 0x99, 0x57, 0xE8, 0x35, 0xAD, 0x55, 0xB2, 0x2E, 0x75, 0xBF, 0x57, 0xBB,
    0x55, 0x6A, 0xC8,
];
const ITERS: usize = 20;

fn main() {
    let do_sign = std::env::args().nth(1).as_deref() == Some("kgsign");
    let mut sink = 0u8;
    for _ in 0..ITERS {
        ctrdrbg::randombytes_init(&SEED);
        let (pk, sk) = crypto_sign_keypair().unwrap();
        sink ^= pk[0] ^ sk[0];
        if do_sign {
            let sm = crypto_sign(&MSG, &sk).unwrap();
            sink ^= sm[0];
        }
    }
    std::hint::black_box(sink);
}
